#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    NSString *theKMLDirectoryPath = [[NSBundle mainBundle] pathForResource:@"kml" ofType:nil];
    NSString *theKMLPath = [self.contentsOfKMLDirectory objectAtIndex:indexPath.row];
    NSString *theFullPath = [theKMLDirectoryPath stringByAppendingPathComponent:theKMLPath];
    KMLParser *theKMLParser = [KMLParser parseKMLAtPath:theFullPath];
    KMLViewerViewController *theKMLViewerViewController = [[KMLViewerViewController alloc] initWithNibName:@"KMLViewerViewController" bundle:nil];
    theKMLViewerViewController.kml = theKMLParser;
    theKMLViewerViewController.title = theKMLPath;
    [self.navigationController pushViewController:theKMLViewerViewController animated:YES];
}

